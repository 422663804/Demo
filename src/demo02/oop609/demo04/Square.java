package demo02.oop609.demo04;

public class Square {
    private double length01;
    private double width;

    public Square(double length01, double width) {
        this.length01 = length01;
        this.width = width;
    }

    public double getLength01() {
        return length01;
    }

    public void setLength01(double length01) {
        this.length01 = length01;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }
}
